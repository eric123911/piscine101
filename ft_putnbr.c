void	ft_putnbr(int nb)
{
	int	f;

	f = 0;
	if (nb == -2147483648)
	{
	  nb %= 1000000000;
	  f = 1;
        }
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	(f == 1) ? ft_putchar('2'), f = 0 : 0;
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + 48);
	}
	else
	{
		ft_putchar(nb % 10 + '0');
	}
}
